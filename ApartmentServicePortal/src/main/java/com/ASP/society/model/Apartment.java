package com.ASP.society.model;

public class Apartment {

	
	private String apartmentName;
	private String TotalNumberOfFlats;
	private String apartmentAddress;
	private String city;
	private String vacantFlats;
	private int doubleBedRooms;
	private int tripleBedRooms;
	private int QuadBedRooms;
	private int floors;
	private int Blocks;
	private int flatsPerFloor;
	private String SocietyName;
	
	public String getApartmentName() {
		return apartmentName;
	}
	public void setApartmentName(String apartmentName) {
		this.apartmentName = apartmentName;
	}
	public String getTotalNumberOfFlats() {
		return TotalNumberOfFlats;
	}
	public void setTotalNumberOfFlats(String totalNumberOfFlats) {
		TotalNumberOfFlats = totalNumberOfFlats;
	}
	public String getApartmentAddress() {
		return apartmentAddress;
	}
	public void setApartmentAddress(String apartmentAddress) {
		this.apartmentAddress = apartmentAddress;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getVacantFlats() {
		return vacantFlats;
	}
	public void setVacantFlats(String vacantFlats) {
		this.vacantFlats = vacantFlats;
	}
	public int getDoubleBedRooms() {
		return doubleBedRooms;
	}
	public void setDoubleBedRooms(int doubleBedRooms) {
		this.doubleBedRooms = doubleBedRooms;
	}
	public int getTripleBedRooms() {
		return tripleBedRooms;
	}
	public void setTripleBedRooms(int tripleBedRooms) {
		this.tripleBedRooms = tripleBedRooms;
	}
	public int getQuadBedRooms() {
		return QuadBedRooms;
	}
	public void setQuadBedRooms(int quadBedRooms) {
		QuadBedRooms = quadBedRooms;
	}
	public int getFloors() {
		return floors;
	}
	public void setFloors(int floors) {
		this.floors = floors;
	}
	public int getBlocks() {
		return Blocks;
	}
	public void setBlocks(int blocks) {
		Blocks = blocks;
	}
	public int getFlatsPerFloor() {
		return flatsPerFloor;
	}
	public void setFlatsPerFloor(int flatsPerFloor) {
		this.flatsPerFloor = flatsPerFloor;
	}
	public String getSocietyName() {
		return SocietyName;
	}
	public void setSocietyName(String societyName) {
		SocietyName = societyName;
	}
	
	
	
	
}
